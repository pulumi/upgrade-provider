func TestInspectLocalUpgrade(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	runGit := func(args ...string) {
		t.Helper()
		cmd := exec.Command("git", append([]string{"-C", dir}, args...)...)
		output, err := cmd.CombinedOutput()
		require.NoError(t, err, string(output))
	}

	runGit("init", "--initial-branch=main")
	runGit("config", "user.name", "Upgrade Provider Test")
	runGit("config", "user.email", "upgrade-provider@example.com")
	require.NoError(t, os.WriteFile(filepath.Join(dir, "result.txt"), []byte("base\n"), 0o600))
	runGit("add", "result.txt")
	runGit("commit", "-m", "base")
	// Model the remote base without requiring a real remote repository.
	runGit("update-ref", "refs/remotes/origin/main", "HEAD")
	require.NoError(t, os.WriteFile(filepath.Join(dir, "result.txt"), []byte("upgraded\n"), 0o600))
	runGit("add", "result.txt")
	runGit("commit", "-m", "upgrade")

	state := inspectLocalUpgrade(context.Background(), ProviderRepo{root: dir, defaultBranch: "main"})
	assert.Equal(t, "origin/main", state.BaseRef)
	assert.Equal(t, "clean", state.WorkingTree)
	assert.Equal(t, "1", state.CommitsAhead)

	require.NoError(t, os.WriteFile(filepath.Join(dir, "uncommitted.txt"), []byte("dirty\n"), 0o600))
	assert.Equal(t, "dirty",
		inspectLocalUpgrade(context.Background(), ProviderRepo{root: dir, defaultBranch: "main"}).WorkingTree)
}

